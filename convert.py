def transform_matrix(matrix):
    # Преобразуем каждый элемент матрицы: 0 -> '-', 1 -> '+'
    transformed = []
    for row in matrix:
        new_row = []
        for char in row:
            if char == '0':
                new_row.append('-')
            elif char == '1':
                new_row.append('+')
        transformed.append(new_row)
    
    # Добавляем столбец '+' слева
    for row in transformed:
        row.insert(0, '+')
    
    # Добавляем строку '+' сверху
    top_row = ['+'] * len(transformed[0])
    transformed.insert(0, top_row)
    
    return transformed

